type Permutation<
T, // the original Union to execute permutation on
K = T // copy of K to run the iterations on
> = 
  [T] extends [never] // never is an empty union and ExtendsNever is a distributive conditional type. As a result `T extends never` would resolve to never
    ? [] 
    : K extends K // conditional types on generics are distributive. It's basically a for in loop
      ? [K, ...Permutation<Exclude<T,K>>]  // add current iteration to the result and run recursive iteration
      : never 

